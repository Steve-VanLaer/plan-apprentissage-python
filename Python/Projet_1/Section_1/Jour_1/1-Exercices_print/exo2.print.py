"""
Cette série d'exercices montrent comment utiliser le paramètre end dans différentes situations
"""
# Par défaut le PS exécute deux (ou plusieurs) corps de texte sur deux lignes
print("A")
print("B")

# Rassembler deux (ou plusieurs) corps de textes
print("A",end="")
print("B")

# Relier deux (ou plusieurs) corps de textes par un signe
print("A",end=",")
print("B")

# Séparer deux (ou plusieurs) corps de textes par un (ou plusieurs) saut(s) de ligne
print("A",end="\n")
print("B")