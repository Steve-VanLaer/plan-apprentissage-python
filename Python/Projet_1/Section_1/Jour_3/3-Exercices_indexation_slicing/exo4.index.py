# Ajouter un élément à la dernière place d'une liste
liste = [1,2,3]
liste.append(4)     # ajouter l'élément 4
print(liste)

# Ajouter un élément à un index précis d'une liste
liste.insert(0,0)    # (élément à ajouter,index où placer l'élément)
print(liste)

# Ajouter plusieurs éléments à une liste
nouvelle_liste = liste + [5,6,7]
print(nouvelle_liste)

